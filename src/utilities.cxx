#include "utilities.h"

std::string timeString(Timestamp& timestamp) {
  time_t now = std::chrono::system_clock::to_time_t(timestamp);
  char buf[80];
  std::strftime(buf, sizeof(buf), "%Y-%m-%d %X UTC", std::gmtime(&now));
  return buf;
}
